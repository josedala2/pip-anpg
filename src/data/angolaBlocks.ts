export type BlockPhase = "Exploration" | "Development" | "Production" | "Suspended" | "Bidding";
export type WaterDepth = "Onshore" | "Shallow Water" | "Deep Water" | "Ultra-Deep Water";

export interface ConcessionPartner {
  name: string;
  share: number; // percentage
  isOperator?: boolean;
}

export interface SeismicData {
  year: number;
  seismic2D: number;
  seismic3D: number;
  seismic4D: number;
}

export interface WellData {
  year: number;
  pesquisa: number;
  avaliacao: number;
}

export interface FieldDiscovery {
  name: string;
  status: "Producing" | "Development" | "Discovery" | "Abandoned";
  discoveryYear?: number;
  peakProduction?: number; // BOPD
}

export interface Prospect {
  discoveryArea: string;
  name: string;
  reservoir: string;
  resourcesMMBO: number;
  resourcesBCF?: number;
  pos: number; // probability of success %
}

export interface OilBlock {
  id: string;
  name: string;
  operator: string;
  partners: string[];
  basin: string;
  phase: BlockPhase;
  waterDepth: WaterDepth;
  contractDate: string;
  dailyProduction: number; // BOPD
  estimatedReserves: number; // million barrels
  accumulatedInvestment: number; // million USD
  plannedInvestment: number; // million USD
  executionRate: number; // percentage
  riskScore: number; // 1-10
  complianceScore: number; // percentage
  concession: ConcessionPartner[];
  mapPosition: { x: number; y: number };
  productionHistory: { month: string; value: number }[];
  capexHistory: { year: string; planned: number; actual: number }[];
  projections: {
    conservative: number[];
    base: number[];
    expansion: number[];
  };
  // Extended exploration data
  seismicData?: SeismicData[];
  wellsData?: WellData[];
  geologicalObjectives?: string[];
  fields?: FieldDiscovery[];
  areaKm2?: number;
  waterDepthRange?: string; // e.g., "200-1500m"
  prospects?: Prospect[];
}

