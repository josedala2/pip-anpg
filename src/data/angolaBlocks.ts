export type BlockPhase = "Exploration" | "Development" | "Production" | "Suspended" | "Bidding";
export type WaterDepth = "Onshore" | "Shallow Water" | "Deep Water" | "Ultra-Deep Water";

export interface ConcessionPartner {
  name: string;
  share: number; // percentage
  isOperator?: boolean;
}

export interface SeismicData {
  year: number;
  seismic2D: number;
  seismic3D: number;
  seismic4D: number;
}

export interface WellData {
  year: number;
  pesquisa: number;
  avaliacao: number;
  descobertaComercial?: number;
  descobertaNaoComercial?: number;
  seco?: number;
}

export interface FieldDiscovery {
  name: string;
  status: "Producing" | "Development" | "Discovery" | "Abandoned";
  discoveryYear?: number;
  peakProduction?: number; // BOPD
}

export interface Prospect {
  discoveryArea: string;
  name: string;
  reservoir: string;
  resourcesMMBO: number;
  resourcesBCF?: number;
  pos: number; // probability of success %
}

export interface FiscalConditions {
  costRecoveryPreProd?: number; // percentage
  costRecoveryPostProd?: number; // percentage
  irp?: number; // Imposto sobre Rendimento de Petróleo %
  ipp?: number; // Imposto de Produção de Petróleo %
  itp?: number; // Imposto de Transação de Petróleo %
  productionPremium?: number; // USD/barrel
  investmentPremiumAreaA?: number; // percentage
  investmentPremiumAreaB?: number; // percentage
  investmentPremiumReduction?: string; // description of reduction conditions
  irpNoteAngolan?: string; // special rate for Angolan companies
}

export interface ResearchPeriod {
  initialPhaseYears?: number;
  initialPhaseWells?: number;
  seismic3dKm2?: number;
  seismic3dReprocKm2?: number;
  subsequentPhaseYears?: number;
  subsequentPhaseWells?: number;
}

export interface ExplorationSummary {
  totalSeismic2DKm?: number;
  totalSeismic3DKm2?: number;
  totalSeismic4DKm2?: number;
  totalWellsPesquisa?: number;
  totalWellsAvaliacao?: number;
  commercialDiscoveries?: number;
  nonCommercialDiscoveries?: number;
  dryWells?: number;
  geologicalSuccessRate?: number; // percentage
  stooipMMBO?: number;
  explorationCostsUSD?: number;
  prospectivitySTOOIPMMBOE?: number;
  prospectivityNote?: string;
  complexity?: string[];
  geologicalTargets?: string; // e.g. "Cretáceo (Pinda/Pós-sal, Toca e Lucula/Pré-sal)"
}

export interface LegislationDocument {
  title: string;
  type: "decreto-lei" | "contrato" | "despacho" | "resolução" | "nota" | "outro";
  reference?: string;
  date?: string;
  description?: string;
  url?: string;
}

export interface ContractInfo {
  decretoLei?: string;
  contractType?: string;
  signingDate?: string;
  effectiveDate?: string;
  location?: string;
  initialConsortium?: ConcessionPartner[];
  signatureBonus?: number;
  socialBonus?: number;
  socialProjects?: number;
  socialProjectsPeriod?: string;
  regulatoryContribution?: number;
  regulatoryContributionPeriod?: string;
  productionBonus?: number;
  productionPeriodStart?: string;
  productionPeriodEnd?: string;
  researchPeriod?: ResearchPeriod;
  fiscalConditions?: FiscalConditions;
  historicalNotes?: string[];
}

export interface InvestmentPlanYear {
  year: number;
  exploracao: number;    // MMUSD
  desenvolvimento: number;
  operacao: number;
  total: number;
}

export interface EconomicData {
  costHistory?: {
    period: string;
    capex: number;   // MMUSD
    opex: number;
  }[];
  investmentPlan?: InvestmentPlanYear[];
  productionShareGE?: { year: number; mmbo: number }[];
  abandonment?: {
    total: number;
    fundingRequired: number;
    fundingDeposited: number;
  };
  opexPerBarrel?: number;   // USD/BO
  opexPerBarrelYear?: number;
  sonangolDebt?: number;
  stateRevenueShare?: { period: string; percentage: number }[];
  observations?: string[];
}

export interface OilBlock {
  id: string;
  name: string;
  operator: string;
  partners: string[];
  basin: string;
  phase: BlockPhase;
  waterDepth: WaterDepth;
  contractDate: string;
  dailyProduction: number; // BOPD
  estimatedReserves: number; // million barrels
  accumulatedInvestment: number; // million USD
  plannedInvestment: number; // million USD
  executionRate: number; // percentage
  riskScore: number; // 1-10
  complianceScore: number; // percentage
  concession: ConcessionPartner[];
  mapPosition: { x: number; y: number };
  productionHistory: { month: string; value: number }[];
  capexHistory: { year: string; planned: number; actual: number }[];
  projections: {
    conservative: number[];
    base: number[];
    expansion: number[];
  };
  // Extended exploration data
  seismicData?: SeismicData[];
  wellsData?: WellData[];
  geologicalObjectives?: string[];
  fields?: FieldDiscovery[];
  areaKm2?: number;
  waterDepthRange?: string; // e.g., "200-1500m"
  prospects?: Prospect[];
  contractInfo?: ContractInfo;
  explorationSummary?: ExplorationSummary;
  legislationDocs?: LegislationDocument[];
  economicData?: EconomicData;
}

